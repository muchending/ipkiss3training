import ipkiss3.all as i3
import os
import datetime


def _ruby_main_layout(top_cell_name, grids_per_unit):
    """Preamble of the ruby script
    :param top_cell_name: name of the topcell
    :param grids_per_unit: grid points per unit
    :return: ruby code
    """
    code = """
 # This file has been generated by IPKISS on {date}
 # Do not modify this file directly, instead, regenerate it using merge_with_klayout   
 # Open the main layout

 main_layout = RBA::Layout.new
 main_layout.dbu = {unit}
 main_layout.create_cell("{top_cell_name}")
 top = main_layout.cell_by_name("{top_cell_name}")


 lmap_default = {{}}
 linfo_to_index = {{}}
 pmap_default = {{}}
""".format(date=datetime.date.today(), top_cell_name=top_cell_name, unit=1/grids_per_unit)  # fmt: skip
    return code


def _ruby_copy_cell_function(unprefixed_cells):
    """Creates Ruby code to copy the entire content of a cell
    :param unprefixed_cells: Cells that don't need prefixing
    :return: String of Ruby code
    """
    code = """
 def copy_cells(lsrc, ltarget, lmap, pmap, prefix, unprefixed_cells={})
   citarget = nil
   to_copy = []

   # a map for the cell indices
   cmap = {{}}
   
   # create a new cell in the target layout and add to the cell index map
   lsrc.each_cell_bottom_up do |cisrc|
     csrc = lsrc.cell(cisrc)
     cell_name = lsrc.cell_name(cisrc)
     if unprefixed_cells.include?(cell_name) || csrc.caller_cells.any? {{ |ci| unprefixed_cells.include?(lsrc.cell_name(ci)) }}
        full_cell_name = cell_name
     else
        full_cell_name =  prefix + lsrc.cell_name(cisrc)
     end    
     ctarget = ltarget.cell(full_cell_name)
     if ctarget.nil?
        to_copy.push(cisrc)
        ctarget = ltarget.create_cell(full_cell_name)
     end

     citarget = ctarget.cell_index
     cmap[cisrc] = citarget
   end     

   to_copy.each do |cisrc|
     csrc = lsrc.cell(cisrc)
     ctarget = ltarget.cell(cmap[cisrc])
     
     # copy the shapes
     lsrc.layer_indices.each do |lisrc|
        shtarget = ctarget.shapes(lmap[lisrc])
        csrc.shapes(lisrc).each do |shape|

          # property mapping 
          newpid = 0 # =no properties
          if shape.has_prop_id?
            newpid = pmap[shape.prop_id]
            if !newpid
              newpid = ltarget.properties_id(lsrc.properties(shape.prop_id))
              pmap[shape.prop_id] = newpid
            end
          end

          newshape = shtarget.insert(shape) 
          shtarget.replace_prop_id(newshape, newpid)
        end
     end
     
     # translate and copy the instances
     csrc.each_inst do |inst|

       # property mapping 
       newpid = 0 # =no properties
       if inst.has_prop_id?
         newpid = pmap[inst.prop_id]
         if !newpid
           newpid = ltarget.properties_id(lsrc.properties(inst.prop_id))
           pmap[inst.prop_id] = newpid
         end
       end

       # get the instance object and create a new one with the new cell index
       i = inst.cell_inst
       trans = i.is_complex? ? i.cplx_trans : i.trans
       cinew = cmap[i.cell_index]
       if i.is_regular_array?
         newinst = ctarget.insert(RBA::CellInstArray.new(cinew, trans, i.a, i.b, i.na, i.nb))
       else
         newinst = ctarget.insert(RBA::CellInstArray.new(cinew, trans))
       end

       # apply the new property id
       ctarget.replace_prop_id(newinst, newpid)

     end

   end

   # Return the last cell's index which (because we iterated bottom up) is a
   # top cell
   return citarget

 end

 def destroy_layout(lsrc)

   citarget = nil

   lsrc.each_cell_bottom_up do |cisrc|

     # create a new cell in the target layout and add to the cell index map
     csrc = lsrc.cell(cisrc)

     # destroy the shapes
     lsrc.layer_indices.each do |lisrc|
       csrc.shapes(lisrc).each do |shape|
         shape.destroy()
       end
     end

     # destroy the instances
     csrc.each_inst do |inst|
       inst.destroy()
     end

   end
 end

    """.format(unprefixed_cells)  # fmt: skip
    return code


def _ruby_layer_map(lo_name, layer_map, output_layer_map):
    """Creates a layer map in klayout to map the layer table of a design to an output layer table
    :param lo_name: name of the layout
    :param layer_map: layer map
    :param output_layer_map: output layer map
    :return:
    """
    map = ""
    map += " lmap_{lo_name} = RBA::LayerMap.new()".format(lo_name=lo_name)
    for pp_layer, gds_l in layer_map.pplayer_map.items():
        output_layer = output_layer_map[i3.PPLayer(pp_layer[0], pp_layer[1])]
        if output_layer.number != gds_l[0] or output_layer.datatype != gds_l[1]:
            map += ' lmap_{lo_name}.map("{iln}/{idt}",l_{lo_name}.insert_layer(RBA::LayerInfo.new({oln},{odt}))) # {proc_name}, {purp_name}'.format(
                iln=gds_l[0],
                idt=gds_l[1],
                oln=output_layer.number,
                odt=output_layer.datatype,
                lo_name=lo_name,
                proc_name=pp_layer.process.extension,
                purp_name=pp_layer.purpose.name,
            )

    return map


def _ruby_merge_design(design, grid_per_unit, output_layer_map=None):
    """Merge design
    :param design: design
    :param grid_per_unit: grid per unit
    :param output_layer_map: output layer map
    :return: ruby code
    """

    merge_string = ""
    transformations = design.get("transformations", [(0, 0, 0, 0)])
    cleaned_transformations = []
    for t in transformations:
        if len(t) >= 3:
            rot = int(round(t[2] / 90.0))
        else:
            rot = 0

        if len(t) >= 4:
            mir = t[3]
        else:
            mir = False
        cleaned_transformations.append((t[0], t[1], rot, mir))

    lo_name = "source"

    merge_string += "#Merging Design {} \n".format(os.path.basename(design.get("source_path")))
    merge_string += """
l_{lo_name} = RBA::Layout.new
 """.format(lo_name=lo_name)  # fmt: skip
    if design.get("layer_map", output_layer_map) and output_layer_map is not None:
        merge_string += _ruby_layer_map(lo_name, design.get("layer_map", output_layer_map), output_layer_map)

    merge_string += """
load_file_options = RBA::LoadLayoutOptions.new()
load_file_options.set_layer_map(lmap_{lo_name}, true)
l_{lo_name}.read("{s_path}", load_file_options)
lmap_{lo_name}.destroy()
lmap_{lo_name} = {{}}
l_{lo_name}.layer_indices.each do |layer|
linfo = l_{lo_name}.get_info(layer)
ltargetlayer = main_layout.insert_layer(linfo)
lmap_{lo_name}[layer] = ltargetlayer;
linfo_to_index[linfo.to_s] = ltargetlayer
end
new_ltop = copy_cells(l_{lo_name}, main_layout, lmap_{lo_name}, pmap_default, "{prefix}_")
coord = RBA::Point.new(({pos_x})*{grid_per_unit},({pos_y})*{grid_per_unit})
main_layout.cell(top).insert(RBA::CellInstArray.new(new_ltop, RBA::Trans.new({rot}, {mir}, coord)))
""".format(
        lo_name=lo_name,
        s_path=os.path.basename(design.get("source_path")),
        prefix=design.get("prefix"),
        pos_x=cleaned_transformations[0][0],
        pos_y=cleaned_transformations[0][1],
        grid_per_unit=grid_per_unit,
        rot=cleaned_transformations[0][2],
        mir="true" if cleaned_transformations[0][3] else "false",
    )

    if len(cleaned_transformations) > 1:
        for rtrans in cleaned_transformations[1:]:
            merge_string += """
    # reference found on design {lo_name}
     coord = RBA::Point.new(({pos_x})*{grid_per_unit},({pos_y})*{grid_per_unit})
     main_layout.cell(top).insert(RBA::CellInstArray.new(new_ltop, RBA::Trans.new({rot}, {mir}, coord)))
     """.format(
                lo_name=lo_name,
                pos_x=rtrans[0],
                pos_y=rtrans[1],
                grid_per_unit=grid_per_unit,
                rot=rtrans[2],
                mir="true" if rtrans[3] else "false",
            )
    return merge_string


def _ruby_final_lines(output_gds):
    """
    Final lines of the Ruby script
    :param output_gds: name of the ouput gds
    :return: ruby code
    """
    final = """
 gds_filename = "{output_gds}"
 slo = RBA::SaveLayoutOptions.new()
 slo.gds2_max_cellname_length = 50
 slo.gds2_max_vertex_count = 4000
 slo.gds2_libname = "SIPPLIB"
 slo.no_empty_cells = true

 main_layout.write(gds_filename, false, slo) 
        """.format(output_gds=output_gds)  # fmt: skip

    return final
